#include "fractol.h"

void	ft_colors(int kc, t_param *p)
{
	if (kc == K1)
		p->pal = 1.5;
	if (kc == K2)
		p->pal = 2;
	if (kc == K3)
		p->pal = 3;
	if (kc == K4)
		p->pal = 4;
	if (kc == K5)
		p->pal = 5;
}
